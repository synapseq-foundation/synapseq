func TestBuilderLoadPreservesOrder(t *testing.T) {
	dir := t.TempDir()
	t.Chdir(dir)
	for _, name := range []string{"rain.wav", "wind.wav"} {
		if err := os.WriteFile(name, nil, 0o600); err != nil {
			t.Fatalf("write temp ambiance %s: %v", name, err)
		}
	}

	builder := New().
		SampleRate(48000).
		Volume(80).
		Ambiance("rain", "rain").
		Ambiance("wind", "wind")

	alpha := builder.NewPreset("alpha")
	alpha.PinkNoise(0).Amplitude(30)

	beta := builder.NewPreset("beta")
	beta.PinkNoise(10).Amplitude(15)

	ctx := synapseq.NewAppContext()
	loaded, err := builder.
		SilenceAt(0).
		PresetAt(15*time.Second, alpha).
		PresetAt(time.Minute, beta).
		Load(ctx)
	if err != nil {
		t.Fatalf("Load error: %v", err)
	}
	content := string(loaded.RawContent())

	expected := strings.Join([]string{
		"# Generated by Synapseq API",
		"",
		"# Options",
		"@samplerate 48000",
		"@volume 80",
		"@ambiance rain rain",
		"@ambiance wind wind",
		"",
		"# Presets",
		"alpha",
		"  noise pink smooth 0.00 amplitude 30.00",
		"beta",
		"  noise pink smooth 10.00 amplitude 15.00",
		"",
		"# Timeline",
		"00:00:00 silence steady 0",
		"00:00:15 alpha steady 0",
		"00:01:00 beta steady 0",
		"",
	}, "\n")

	if content != expected {
		t.Fatalf("unexpected content:\n%s", content)
	}
}

func TestBuilderLoadReturnsValidationError(t *testing.T) {
	builder := New()
	alpha := builder.NewPreset("alpha")
	alpha.Tone(300).Binaural(10).Amplitude(15)

	_, err := builder.PresetAt(99*time.Minute, alpha).Load(synapseq.NewAppContext())
	if err == nil {
		t.Fatal("expected validation error")
	}
}

func TestBuilderLoadRequiresContext(t *testing.T) {
	_, err := New().Load(nil)
	if err == nil {
		t.Fatal("expected nil context error")
	}
}
